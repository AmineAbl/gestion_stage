    private void bnAffectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnAffectActionPerformed
        Stage s = (Stage) listeStage.getSelectedItem();
        Etudiant e = (Etudiant) listeEtudiant.getSelectedItem();
        Encadrant en = (Encadrant) listeEncadrant.getSelectedItem();
        if (asv.create(new beans.AffectationStage(s, e, en))) {
            JOptionPane.showMessageDialog(this, "Affectation réussie ");
            load();
        } else {
            JOptionPane.showMessageDialog(this, "Affectation echoué !! ");
        }
    }//GEN-LAST:event_bnAffectActionPerformed

