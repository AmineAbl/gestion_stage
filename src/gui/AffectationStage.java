    void load() {
        model.setRowCount(0);
        for (beans.AffectationStage as : asv.findAll()) {
            model.addRow(new Object[]{as.getStage().getEntreprise(), as.getEtudiant().getNom() + " " + as.getEtudiant().getPrenom(), as.getEncadrant().getNom() + " " + as.getEncadrant().getPrenom()});
        }

    }

    void loadStage() {
        listeStage.addItem("Choisir une entreprise :");
        for (Stage s : sv.findAll()) {
            listeStage.addItem(s);
        }
    }

    void loadEtudiant() {
        listeEtudiant.addItem("Choisir un etudiant :");
        for (Etudiant e : es.findAll()) {
            listeEtudiant.addItem(e);
        }
    }

    void loadEncadrant() {
        listeEncadrant.addItem("Choisir un encadrant :");
        for (Encadrant e : ens.findAll()) {
            listeEncadrant.addItem(e);
        }
    }

